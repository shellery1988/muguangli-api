package com.muguangli.api.pojo;

import java.util.Date;

public class UserStudy {
    private Integer id;

    private Integer lessonId;

    private String openId;

    private Integer currentTaskStage;

    private Date startTime;

    private Date endTime;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getLessonId() {
        return lessonId;
    }

    public void setLessonId(Integer lessonId) {
        this.lessonId = lessonId;
    }

    public String getOpenId() {
        return openId;
    }

    public void setOpenId(String openId) {
        this.openId = openId == null ? null : openId.trim();
    }

    public Integer getCurrentTaskStage() {
        return currentTaskStage;
    }

    public void setCurrentTaskStage(Integer currentTaskStage) {
        this.currentTaskStage = currentTaskStage;
    }

    public Date getStartTime() {
        return startTime;
    }

    public void setStartTime(Date startTime) {
        this.startTime = startTime;
    }

    public Date getEndTime() {
        return endTime;
    }

    public void setEndTime(Date endTime) {
        this.endTime = endTime;
    }
}